            result_lg['Czy dodać'] = result_lg.apply(lambda row: 'DODAJ' if row['Kod SAP'] not in poprzedni_lg['Kod SAP'].values 
                                                     or row['pakiet'] not in poprzedni_lg['pakiet'].values else '', axis=1)


